function mostrarTela(id) {
    document.querySelectorAll(".tela").forEach(tela => tela.classList.remove("ativa"));
    document.getElementById(id).classList.add("ativa");
}

function carregarLabirinto() {
    const lab = labirintos[faseAtual];
    const elementoLabirinto = document.getElementById("labirinto");
    elementoLabirinto.innerHTML = "";
    document.getElementById("exibeFase").innerText = "Fase " + (faseAtual + 1) + " de 6";

    elementoLabirinto.style.gridTemplateColumns = `repeat(${lab[0].length}, 25px)`; // define o tamanho das matrizes
    elementoLabirinto.style.gridTemplateRows = `repeat(${lab.length}, 25px)`;

    for (let i = 0; i < lab.length; i++) {
        for (let j = 0; j < lab[i].length; j++) {
            const celula = document.createElement("div");
            celula.classList.add("celula");
            if (lab[i][j] === 1) celula.classList.add("parede");
            else celula.classList.add("livre");
            if (lab[i][j] === 2) celula.classList.add("saida");
            if (i === posicaoJogador.linha && j === posicaoJogador.coluna)
                celula.classList.add("jogador");
            elementoLabirinto.appendChild(celula);
        }
    }
}

